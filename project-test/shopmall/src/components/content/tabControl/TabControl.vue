<template>
    <div class="tab-control">
        <div class="tab-control-item" v-for="(item,index) in titles"
             :class="{active: index === currentIndex}"
             @click="checkTabControl(index)">
            <span>{{item}}</span>
        </div>
    </div>
</template>

<script>
    export default {
        name: "TabControl",
        data(){
            return {
                currentIndex: 0
            }
        },
        props:{
            titles:{
                type: Array,
                default() {
                    return [];
                }
            }
        },
        methods:{
            checkTabControl(index){
                this.currentIndex = index
                this.$emit('itemClick',index)
            }
        }
    }
</script>

<style scoped>
    .tab-control{
        display: flex;
        text-align: center;
